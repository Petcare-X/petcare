import { useEffect, useState } from "react";
import type { HealthResponse, Item } from "@monorepo/shared-types";

/**
 * Главный компонент приложения (App)
 * 
 * Отвечает за:
 * - Загрузку данных о здоровье (health) бэкенда при инициализации
 * - Загрузку списка товаров (items) при инициализации
 * - Управление состоянием загрузки и ошибок
 * - Отображение данных в пользовательском интерфейсе
 */
function App() {
  // ── Состояние: Здоровье бэкенда ──────────────────────────────────────────
  // Хранит ответ о статусе здоровья бэкенда или null, если данные ещё не загружены
  const [health, setHealth] = useState<HealthResponse | null>(null);
  
  // ── Состояние: Список товаров ────────────────────────────────────────────
  // Хранит массив товаров, изначально пустой
  const [items, setItems] = useState<Item[]>([]);
  
  // ── Состояние: Ошибка при загрузке здоровья ────────────────────────────
  // Хранит текст ошибки при попытке получить статус бэкенда или null если ошибки нет
  const [healthError, setHealthError] = useState<string | null>(null);
  
  // ── Состояние: Ошибка при загрузке товаров ──────────────────────────────
  // Хранит текст ошибки при попытке получить список товаров или null если ошибки нет
  const [itemsError, setItemsError] = useState<string | null>(null);
  
  // ── Состояние: Флаг загрузки ─────────────────────────────────────────────
  // Индикатор того, идёт ли загрузка товаров. Используется для отображения "Loading..."
  const [loading, setLoading] = useState(true);

  /**
   * Эффект инициализации (useEffect)
   * 
   * Выполняется один раз при монтировании компонента (пустой массив зависимостей [])
   * 
   * Задачи:
   * 1. Загрузить статус здоровья бэкенда с эндпоинта /api/health
   * 2. Загрузить список товаров с эндпоинта /api/items
   * 3. Обработать ошибки при загрузке
   */
  useEffect(() => {
    // ─── Запрос статуса здоровья бэкенда ──────────────────────────────────
    // Выполняется параллельно с запросом товаров, не блокирует друг друга
    fetch("/api/health")
      .then((res) => res.json())
      .then((data: HealthResponse) => setHealth(data))
      .catch((err: unknown) => {
        // Преобразуем ошибку в строку для отображения пользователю
        setHealthError(err instanceof Error ? err.message : "Unknown error");
      });

    // ─── Запрос списка товаров ────────────────────────────────────────────
    // Основной запрос данных, после его завершения отключаем флаг loading
    fetch("/api/items")
      .then((res) => res.json())
      .then((data: Item[]) => {
        // Успешная загрузка: сохраняем товары и отключаем индикатор загрузки
        setItems(data);
        setLoading(false);
      })
      .catch((err: unknown) => {
        // Ошибка при загрузке: сохраняем сообщение об ошибке и отключаем индикатор
        setItemsError(err instanceof Error ? err.message : "Unknown error");
        setLoading(false);
      });
  }, []);

  return (
    <div>
      {/* ─── Заголовок приложения ──────────────────────────────────────────── */}
      <h1>🚀 Monorepo App</h1>
      <p style={{ color: "#aaa", marginBottom: "2rem" }}>
        React + Vite + FastAPI + Turborepo
      </p>

      {/* ═══════════════════════════════════════════════════════════════════════
          СЕКЦИЯ 1: Статус здоровья бэкенда (Backend Health)
          
          Отображает:
          - Если есть ошибка: сообщение об ошибке подключения
          - Если данные загружены: карточка с статусом, сообщением и временем
          - Если данные загружаются: текст "Connecting to backend..."
          ═════════════════════════════════════════════════════════════════════ */}
      <div className="section">
        <h2>Backend Health</h2>
        {/* Условное отображение на основе наличия ошибки */}
        {healthError ? (
          // ── Вариант 1: Ошибка при подключении к бэкенду ──────────────────
          <p className="error-text">
            ❌ Could not reach backend: {healthError}
          </p>
        ) : health ? (
          // ── Вариант 2: Успешная загрузка статуса здоровья ───────────────
          <div className="card">
            {/* Статус с динамическим классом для стилизации */}
            <p>
              Status:{" "}
              <span
                className={
                  health.status === "ok" ? "status-ok" : "status-error"
                }
              >
                {/* Эмодзи меняется в зависимости от статуса */}
                {health.status === "ok" ? "✅" : "❌"} {health.status}
              </span>
            </p>
            {/* Сообщение от бэкенда */}
            <p>Message: {health.message}</p>
            {/* Временная метка (серый текст, меньший размер) */}
            <p style={{ color: "#aaa", fontSize: "0.85em" }}>
              {health.timestamp}
            </p>
          </div>
        ) : (
          // ── Вариант 3: Данные загружаются ───────────────────────────────
          <p style={{ color: "#aaa" }}>Connecting to backend...</p>
        )}
      </div>

      {/* ═══════════════════════════════════════════════════════════════════════
          СЕКЦИЯ 2: Список товаров (Items)
          
          Отображает:
          - Если есть ошибка: сообщение об ошибке загрузки
          - Если загружаются: текст "Loading items..."
          - Если товаров нет: текст "No items found."
          - Если товары есть: сетка карточек с информацией о каждом товаре
          ═════════════════════════════════════════════════════════════════════ */}
      <div className="section">
        <h2>Items</h2>
        {/* Многоуровневое условное отображение для различных состояний */}
        {itemsError ? (
          // ── Вариант 1: Ошибка при загрузке товаров ──────────────────────
          <p className="error-text">❌ Could not load items: {itemsError}</p>
        ) : loading ? (
          // ── Вариант 2: Товары загружаются ──────────────────────────────
          <p style={{ color: "#aaa" }}>Loading items...</p>
        ) : items.length === 0 ? (
          // ── Вариант 3: Товаров нет в базе ────────────────────────────
          <p style={{ color: "#aaa" }}>No items found.</p>
        ) : (
          // ── Вариант 4: Товары успешно загружены ──────────────────────
          <div className="items-grid">
            {/* Отображаем каждый товар в виде карточки */}
            {items.map((item) => (
              // ─── Карточка товара ──────────────────────────────────────────
              <div key={item.id} className="card">
                {/* Название товара */}
                <h3>{item.name}</h3>
                
                {/* Описание товара (опциональное поле) */}
                {item.description && (
                  <p style={{ color: "#aaa", margin: "0.4rem 0" }}>
                    {item.description}
                  </p>
                )}
                
                {/* Цена товара, отформатированная до 2 десятичных знаков */}
                <p style={{ marginTop: "0.5rem" }}>
                  💰 ${item.price.toFixed(2)}
                </p>
                
                {/* Статус наличия товара с динамической стилизацией (зеленый/красный) */}
                <p style={{ marginTop: "0.4rem" }}>
                  <span
                    className={`badge ${item.in_stock ? "badge-green" : "badge-red"}`}
                  >
                    {item.in_stock ? "In Stock" : "Out of Stock"}
                  </span>
                </p>
              </div>
            ))}
          </div>
        )}
      </div>
    </div>
  );
}

export default App;

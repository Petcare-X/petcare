# ─────────────────────────────────────────────────────────────────────────────
# Тест проверки эндпоинта здоровья сервиса
# ─────────────────────────────────────────────────────────────────────────────
async def test_health_check(client: AsyncClient) -> None:
    """
    Проверить, что эндпоинт /api/health возвращает правильный статус.
    
    Тест убедится, что:
    - Статус ответа равен 200 (OK)
    - В ответе присутствует статус "ok"
    - В ответе присутствует сообщение "Service is running"
    - В ответе присутствует временная метка
    """
    # Отправляем GET запрос к эндпоинту здоровья
    response = await client.get("/api/health")
    
    # Проверяем, что сервер ответил с кодом 200
    assert response.status_code == 200
    
    # Парсим JSON ответ
    data = response.json()
    
    # Проверяем, что статус равен "ok"
    assert data["status"] == "ok"
    
    # Проверяем, что сообщение равно "Service is running"
    assert data["message"] == "Service is running"
    
    # Проверяем, что в ответе присутствует временная метка
    assert "timestamp" in data


# ─────────────────────────────────────────────────────────────────────────────
# Тест корневого маршрута приложения
# ─────────────────────────────────────────────────────────────────────────────
async def test_root(client: AsyncClient) -> None:
    """
    Проверить, что корневой маршрут "/" работает корректно.
    
    Тест убедится, что:
    - Статус ответа равен 200 (OK)
    - В ответе присутствует поле "message"
    """
    # Отправляем GET запрос к корневому маршруту
    response = await client.get("/")
    
    # Проверяем, что сервер ответил с кодом 200
    assert response.status_code == 200
    
    # Проверяем, что в ответе присутствует поле "message"
    assert "message" in response.json()

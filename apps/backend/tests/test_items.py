# ─────────────────────────────────────────────────────────────────────────────
# Тест создания нового товара
# ─────────────────────────────────────────────────────────────────────────────
async def test_create_item(client: AsyncClient) -> None:
    """
    Проверить, что эндпоинт POST /api/items создаёт новый товар.
    
    Тест убедится, что:
    - Статус ответа равен 201 (Created)
    - В ответе содержится созданный товар с правильными данными
    - Товару присвоен уникальный ID
    """
    # Подготавливаем данные для создания нового товара
    payload = {
        "name": "Test Item",
        "description": "A test",
        "price": 1.99,
        "in_stock": True
    }
    
    # Отправляем POST запрос для создания товара
    response = await client.post("/api/items", json=payload)
    
    # Проверяем, что сервер ответил с кодом 201 (Created)
    assert response.status_code == 201
    
    # Парсим JSON ответ
    data = response.json()
    
    # Проверяем, что название товара совпадает с отправленным
    assert data["name"] == "Test Item"
    
    # Проверяем, что цена товара совпадает с отправленной
    assert data["price"] == 1.99
    
    # Проверяем, что товару присвоен ID
    assert "id" in data


# ─────────────────────────────────────────────────────────────────────────────
# Тест удаления товара
# ─────────────────────────────────────────────────────────────────────────────
async def test_delete_item(client: AsyncClient) -> None:
    """
    Проверить, что эндпоинт DELETE /api/items/{item_id} удаляет товар.
    
    Тест выполняет следующие шаги:
    1. Создаёт новый товар через POST запрос
    2. Удаляет созданный товар через DELETE запрос
    3. Проверяет, что товар больше не доступен через GET запрос
    """
    # ─── Шаг 1: Создаём товар ──────────────────────────────────────────────
    # Подготавливаем данные для создания товара
    payload = {"name": "To Delete", "price": 0.99}
    
    # Отправляем POST запрос для создания товара
    create_resp = await client.post("/api/items", json=payload)
    
    # Проверяем, что товар успешно создан
    assert create_resp.status_code == 201
    
    # Получаем ID созданного товара
    item_id = create_resp.json()["id"]

    # ─── Шаг 2: Удаляем товар ──────────────────────────────────────────────
    # Отправляем DELETE запрос для удаления товара
    delete_resp = await client.delete(f"/api/items/{item_id}")
    
    # Проверяем, что товар успешно удалён (статус 204 - No Content)
    assert delete_resp.status_code == 204

    # ─── Шаг 3: Проверяем, что товар удалён ────────────────────────────────
    # Отправляем GET запрос для попытки получения удалённого товара
    get_resp = await client.get(f"/api/items/{item_id}")
    
    # Проверяем, что сервер возвращает ошибку 404
    assert get_resp.status_code == 404



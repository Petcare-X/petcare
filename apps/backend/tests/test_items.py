# ─────────────────────────────────────────────────────────────────────────────
# Тест получения списка всех товаров
# ─────────────────────────────────────────────────────────────────────────────
async def test_list_items(client: AsyncClient) -> None:
    """
    Проверить, что эндпоинт GET /api/items возвращает список товаров.
    
    Тест убедится, что:
    - Статус ответа равен 200 (OK)
    - Ответ содержит список
    - В списке как минимум 1 товар
    """
    # Отправляем GET запрос к эндпоинту получения списка товаров
    response = await client.get("/api/items")
    
    # Проверяем, что сервер ответил с кодом 200
    assert response.status_code == 200
    
    # Парсим JSON ответ
    data = response.json()
    
    # Проверяем, что ответ является списком
    assert isinstance(data, list)
    
    # Проверяем, что в списке есть хотя бы один товар
    assert len(data) >= 1



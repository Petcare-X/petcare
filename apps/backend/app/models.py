# ─────────────────────────────────────────────────────────────────────────────
# Импорты необходимых модулей для создания моделей данных
# ─────────────────────────────────────────────────────────────────────────────
from pydantic import BaseModel, Field


# ─────────────────────────────────────────────────────────────────────────────
# Модель ответа для эндпоинта проверки здоровья сервиса
# ─────────────────────────────────────────────────────────────────────────────
class HealthResponse(BaseModel):
    """
    Модель ответа на запрос проверки здоровья API.
    
    Атрибуты:
        status (str): Статус сервиса ("ok")
        message (str): Описание статуса сервиса
        timestamp (str): Время проверки в формате ISO 8601
    """
    status: str = Field(..., examples=["ok"])
    message: str = Field(..., examples=["Service is running"])
    timestamp: str = Field(..., examples=["2024-01-01T00:00:00Z"])


# ─────────────────────────────────────────────────────────────────────────────
# Модель товара в каталоге
# Используется для представления товара в базе данных и API ответах
# ─────────────────────────────────────────────────────────────────────────────
class Item(BaseModel):
    """
    Модель товара для работы с каталогом.
    
    Атрибуты:
        id (int): Уникальный идентификатор товара
        name (str): Название товара
        description (str | None): Описание товара (опционально)
        price (float): Цена товара (должна быть больше 0)
        in_stock (bool): Флаг доступности товара (по умолчанию True)
    """
    id: int
    name: str
    description: str | None = None
    price: float = Field(..., gt=0)  # Цена должна быть положительной
    in_stock: bool = True


# ─────────────────────────────────────────────────────────────────────────────
# Модель запроса на создание нового товара
# Используется для валидации данных при создании товара через API
# ─────────────────────────────────────────────────────────────────────────────
class CreateItemRequest(BaseModel):
    """
    Модель для создания нового товара.
    
    Атрибуты:
        name (str): Название товара (от 1 до 100 символов)
        description (str | None): Описание товара (опционально)
        price (float): Цена товара (должна быть больше 0)
        in_stock (bool): Флаг доступности товара (по умолчанию True)
    """
    name: str = Field(..., min_length=1, max_length=100)  # Валидация длины названия
    description: str | None = None
    price: float = Field(..., gt=0)  # Цена должна быть положительной
    in_stock: bool = True


# ─────────────────────────────────────────────────────────────────────────────
# Универсальная модель API ответа
# Используется для обёртывания данных ответа с информацией об успехе
# ─────────────────────────────────────────────────────────────────────────────
class ApiResponse[T](BaseModel):
    """
    Универсальная модель ответа API.
    
    Параметры типа:
        T: Тип данных, которые содержит ответ
    
    Атрибуты:
        data (T): Данные ответа (любого типа)
        success (bool): Флаг успеха операции (по умолчанию True)
    """
    data: T
    success: bool = True

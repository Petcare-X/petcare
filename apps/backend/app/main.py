# ─────────────────────────────────────────────────────────────────────────────
# Импорты необходимых модулей FastAPI и промежуточного ПО
# ─────────────────────────────────────────────────────────────────────────────
from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware

# Импорт маршрутизаторов из приложения
from app.routers import health, items

# ─────────────────────────────────────────────────────────────────────────────
# Инициализация FastAPI приложения
# ─────────────────────────────────────────────────────────────────────────────
app = FastAPI(
    title="Monorepo Backend",
    description="FastAPI backend for the monorepo project",
    version="0.1.0",
)

# ─── CORS ─────────────────────────────────────────────────────────────────────
# Добавление промежуточного ПО для обработки Cross-Origin Resource Sharing (CORS)
# Разрешаем запросы с фронтенда (localhost:3000)
app.add_middleware(
    CORSMiddleware,
    allow_origins=["http://localhost:3000"],  # Разрешить запросы только с этого источника
    allow_credentials=True,  # Разрешить отправку cookies
    allow_methods=["*"],  # Разрешить все HTTP методы (GET, POST, DELETE и т.д.)
    allow_headers=["*"],  # Разрешить все заголовки
)

# ─── Routers ──────────────────────────────────────────────────────────────────
# Подключение маршрутизаторов к приложению с общим префиксом /api
app.include_router(health.router, prefix="/api")  # Маршруты проверки здоровья
app.include_router(items.router, prefix="/api")   # Маршруты работы с товарами


# ─────────────────────────────────────────────────────────────────────────────
# Корневой маршрут приложения
# Возвращает приветственное сообщение с информацией о документации
# ─────────────────────────────────────────────────────────────────────────────
@app.get("/")
async def root() -> dict[str, str]:
    """
    Обработчик корневого маршрута.
    
    Возвращает:
        dict[str, str]: Словарь с приветственным сообщением
    """
    return {"message": "Monorepo Backend API. Visit /docs for Swagger UI."}

# ─────────────────────────────────────────────────────────────────────────────
# Импорты необходимых модулей для работы с датой и временем, а также FastAPI
# ─────────────────────────────────────────────────────────────────────────────
from datetime import UTC, datetime

from fastapi import APIRouter

# Импорт модели ответа для проверки здоровья
from app.models import HealthResponse

# ─────────────────────────────────────────────────────────────────────────────
# Создание маршрутизатора для эндпоинтов, связанных с проверкой здоровья
# ─────────────────────────────────────────────────────────────────────────────
router = APIRouter(tags=["health"])  # Тег используется для группировки в документации Swagger


# ─────────────────────────────────────────────────────────────────────────────
# Эндпоинт для проверки здоровья сервиса
# Возвращает текущий статус и время проверки
# ─────────────────────────────────────────────────────────────────────────────
@router.get("/health", response_model=HealthResponse)
async def health_check() -> HealthResponse:
    """
    Проверить статус здоровья API.
    
    Эта функция вызывается мониторингом и балансировщиком нагрузки для проверки,
    что сервис работает корректно.
    
    Возвращает:
        HealthResponse: Модель ответа с статусом, сообщением и временем проверки
    """
    return HealthResponse(
        status="ok",  # Статус сервиса
        message="Service is running",  # Описание статуса
        timestamp=datetime.now(UTC).isoformat(),  # Текущее время в UTC (ISO 8601 формат)
    )

# ─────────────────────────────────────────────────────────────────────────────
# Импорты необходимых модулей для работы с маршрутами и обработкой ошибок
# ─────────────────────────────────────────────────────────────────────────────
from fastapi import APIRouter, HTTPException

# Импорт моделей товара
from app.models import CreateItemRequest, Item

# ─────────────────────────────────────────────────────────────────────────────
# Создание маршрутизатора для эндпоинтов, связанных с товарами
# ─────────────────────────────────────────────────────────────────────────────
router = APIRouter(tags=["items"])  # Тег используется для группировки в документации Swagger

# ─────────────────────────────────────────────────────────────────────────────
# Хранилище товаров в памяти
# ВАЖНО: В production среде это должно быть заменено на настоящую БД (PostgreSQL, MongoDB и т.д.)
# ─────────────────────────────────────────────────────────────────────────────
_items: list[Item] = [
    # Предопределённые товары для демонстрации
    Item(id=1, name="Widget A", description="A fantastic widget", price=9.99, in_stock=True),
    Item(id=2, name="Gadget B", description="A useful gadget", price=24.99, in_stock=True),
    Item(id=3, name="Doohickey C", description=None, price=4.49, in_stock=False),
]

# Счётчик для генерации уникальных идентификаторов новых товаров
_next_id: int = 4


# ─────────────────────────────────────────────────────────────────────────────
# Эндпоинт для получения списка всех товаров
# ─────────────────────────────────────────────────────────────────────────────
@router.get("/items", response_model=list[Item])
async def list_items() -> list[Item]:
    """
    Получить список всех товаров из каталога.
    
    Возвращает:
        list[Item]: Список объектов товаров
    """
    return _items


# ─────────────────────────────────────────────────────────────────────────────
# Эндпоинт для получения одного конкретного товара по его ID
# ─────────────────────────────────────────────────────────────────────────────
@router.get("/items/{item_id}", response_model=Item)
async def get_item(item_id: int) -> Item:
    """
    Получить информацию о конкретном товаре по его ID.
    
    Аргументы:
        item_id (int): Уникальный идентификатор товара
    
    Возвращает:
        Item: Объект товара с запрошенным ID
    
    Исключения:
        HTTPException: Если товар с заданным ID не найден (404)
    """
    # Поиск товара в списке по ID
    for item in _items:
        if item.id == item_id:
            return item
    # Если товар не найден, возвращаем ошибку 404
    raise HTTPException(status_code=404, detail=f"Item {item_id} not found")


# ─────────────────────────────────────────────────────────────────────────────
# Эндпоинт для создания нового товара
# ─────────────────────────────────────────────────────────────────────────────
@router.post("/items", response_model=Item, status_code=201)
async def create_item(body: CreateItemRequest) -> Item:
    """
    Создать новый товар в каталоге.
    
    Аргументы:
        body (CreateItemRequest): Модель с данными нового товара
    
    Возвращает:
        Item: Созданный объект товара с присвоенным ID
    """
    global _next_id  # Используем глобальную переменную для счётчика ID
    
    # Создаём новый объект товара с автоматически сгенерированным ID
    new_item = Item(id=_next_id, **body.model_dump())
    
    # Добавляем товар в список
    _items.append(new_item)
    
    # Увеличиваем счётчик для следующего товара
    _next_id += 1
    
    return new_item


# ─────────────────────────────────────────────────────────────────────────────
# Эндпоинт для удаления товара по его ID
# ─────────────────────────────────────────────────────────────────────────────
@router.delete("/items/{item_id}", status_code=204)
async def delete_item(item_id: int) -> None:
    """
    Удалить товар из каталога по его ID.
    
    Аргументы:
        item_id (int): Уникальный идентификатор товара для удаления
    
    Исключения:
        HTTPException: Если товар с заданным ID не найден (404)
    
    Возвращает:
        None: При успешном удалении возвращается пустой ответ (204 No Content)
    """
    # Ищем товар в списке по ID и удаляем его
    for i, item in enumerate(_items):
        if item.id == item_id:
            _items.pop(i)  # Удаляем товар из списка по индексу
            return
    
    # Если товар не найден, возвращаем ошибку 404
    raise HTTPException(status_code=404, detail=f"Item {item_id} not found")
